package ch03;

//if_else 문법

public class if_else {
    public static void main(String[] args) {
        int number = 1;

        if (number == 1) {
            System.out.println("if 블록입니다.");
        } else if (number == 2) {
            System.out.println("else if 블록입니다.");
        } else {
            System.out.println("else 블록입니다.");
        }
    }
}
